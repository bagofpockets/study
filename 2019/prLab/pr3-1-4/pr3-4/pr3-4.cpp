#define MAX_LOADSTRING 100

class Vector2D
{
private:
	int* x;
	int* y;

	void swap(Vector2D& b)
	{
		std::swap(x, b.x);
		std::swap(y, b.y);
	}

public:
	Vector2D(int x, int y) : x(new int(x)), y(new int(y)) {}
	~Vector2D() { delete x; delete y; }

	friend Vector2D operator+(Vector2D const v1, Vector2D const v2);

	Vector2D& operator = (Vector2D const& n)
	{
		if (this != &n)
		{
			Vector2D(n).swap(*this);
		}
		return *this;
	}

	int get_x() { return *x; }
	int get_y() { return *y; }
	void set_x(int x) { this->x = &x; }
	void set_y(int y) { this->y = &y; }
};

Vector2D operator+(Vector2D v1, Vector2D v2)
{
	v1.set_x(v1.get_x() + v2.get_x());
	v1.set_y(v1.get_y() + v2.get_y());
	return v1;
}

Vector2D v(10, 10);

